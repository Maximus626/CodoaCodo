    public void mostrar() {

        String matriz[][] = new String[lista.size()][5];
        for (int i = 0; i < lista.size(); i++) {
            matriz[i][0] = lista.get(i).getId();
            matriz[i][1] = Integer.toString(lista.get(i).getCantidad());
            matriz[i][2] = lista.get(i).getProducto();
            matriz[i][3] = Double.toString(lista.get(i).getPrecio());
            matriz[i][4] = Double.toString(lista.get(i).getPrecio()*lista.get(i).getCantidad()); 

        }
        String titulo[] = {"Id", "Cantidad", "Producto", "Precio", "Total"};
        //DefaultTableModel modelo = new DefaultTableModel(matriz,titulo);
        modelo.setDataVector(matriz, titulo);
        Tabla.setModel(modelo);

    }

